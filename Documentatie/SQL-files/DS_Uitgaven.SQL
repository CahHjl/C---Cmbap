CREATE TABLE Uitgaven (
  Ugv_Id INTEGER NOT NULL PRIMARY KEY Autoincrement,
  Ugv_StatusId INTEGER DEFAULT 140009 NOT NULL,
  Ugv_DispStatus VARCHAR(75) DEFAULT 'Uitgave-record is leeg / Tabelinitrecord',
  Ugv_KlBgId INTEGER DEFAULT 1 NOT NULL,
  Ugv_DispKlBg VARCHAR(75) DEFAULT 'Klant-Begunstigde',
  Ugv_HusId INTEGER DEFAULT 1 NOT NULL,
  Ugv_DispHus VARCHAR(50),
  Ugv_SusId INTEGER DEFAULT 1 NOT NULL,
  Ugv_DispSus VARCHAR(50),
  Ugv_Kasbewijsnr VARCHAR(10) DEFAULT '0' NOT NULL,
  Ugv_Datum DATETIME DEFAULT '01-01-1900 00:00:00' NOT NULL,
  Ugv_Bedrag CURRENCY,
  Ugv_Opmerking VARCHAR(250),
  FOREIGN KEY(UGV_STATUSID) REFERENCES STATUS(STATUS_CODE) ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY(UGV_HUSID) REFERENCES HOOFDUITGAVESOORT(HUS_ID) ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY(UGV_SUSID) REFERENCES SUBUITGAVESOORT(SUS_ID) ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY(UGV_KLBGID) REFERENCES KLANT_BEGUNSTIGDE(KLBG_ID) ON DELETE CASCADE ON UPDATE CASCADE
 );
CREATE INDEX "IdxUgv_KlBgId" ON Uitgaven (Ugv_KlBgId ASC); 
CREATE INDEX "IdxUgv_HusId" ON Uitgaven (Ugv_HusId ASC); 
CREATE INDEX "IdxUgv_SusId" ON Uitgaven (Ugv_SusId ASC); 
CREATE UNIQUE INDEX "IdxUgv_Kasbewijsnr" ON Uitgaven (Ugv_Kasbewijsnr); 
CREATE INDEX "IdxUgv_DatumKasbewijsnr" ON Uitgaven (Ugv_Datum ASC, Ugv_Kasbewijsnr ASC);
